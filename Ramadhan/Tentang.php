                <h1 class="mb-5">Sejarah Puasa Ramadan Berdasarkan Alquran dan Hadits</h1>
                <p>Sejak zaman Nabi Muhammad SAW, sejarah puasa Ramadan dimulai. Awal mula perintah menunaikan ibadah
                    puasa Ramadan yakni pada bulan Sya'ban tahun kedua Hijriah.Ketika itu, umat Islam berhijrah dari
                    Makkah menuju Madinah dan diperintahkan Allah untuk memindahkan kiblat dari Masjid Al-Aqsa ke
                    Masjidil Haram.</p>

                <p>Menurut hadits yang diriwayatkan Mu'adz bin Jabal, Nabi Muhammad SAW mendapatkan perintah untuk puasa
                    Ramadan setelah melaksanakan puasa 'Asyura dan puasa tiga hari setiap bulan.

                    Hingga akhirnya, puasa Ramadan mulai diwajibkan pada tahun ke-2 Hijriah (624 Masehi), bersamaan
                    dengan syariat salat Idul Fitri, zakat fitrah, dan kurban.</p>

                <p>Selain tercantum pada Q.S Al-Baqarah ayat 183, sejarah puasa Ramadan menjadi wajib dijelaskan
                    pula dalam Q.S Al-Baqarah ayat 185, berbunyi:

                    "(Beberapa hari yang ditentukan itu ialah) bulan Ramadhan, bulan yang di dalamnya diturunkan
                    (permulaan) Alquran sebagai petunjuk bagi manusia dan penjelasan-penjelasan mengenai petunjuk
                    itu dan pembeda (antara yang hak dan yang bathil). Karena itu, barangsiapa di antara kamu hadir
                    (di negeri tempat tinggalnya) di bulan itu, maka hendaklah ia berpuasa pada bulan itu, dan
                    barangsiapa sakit atau dalam perjalanan (lalu ia berbuka), maka (wajiblah baginya berpuasa),
                    sebanyak hari yang ditinggalkannya itu, pada hari-hari yang lain. Allah menghendaki kemudahan
                    bagimu, dan tidak menghendaki kesukaran bagimu. Dan hendaklah kamu mencukupkan bilangannya dan
                    hendaklah kamu mengagungkan Allah atas petunjuk-Nya yang diberikan kepadamu, supaya kamu
                    bersyukur."</p>

                <p>
                    Sejarah puasa Ramadan juga diterangkan dalam dalam beberapa hadits shahih yang menyebutkan bahwa
                    puasa Ramadan bersifat wajib bagi umat Islam.

                    Adapun beberapa hadits tentang kewajiban puasa Ramadan, sebagai berikut:

                    1. Hadits Riwayat Ahmad Nasa’i dan Baihaqi dalam Syu’abul Iman, dishahihkan oleh Al Albani dalam
                    Shahihul Jaami’ Nomor 55.
                </p>
                <p>
                    Dalam hadits ini, Rasulullah shallallahu ‘alaihi wa sallam (SAW) bersabda:

                    "Telah datang kepadamu bulan Ramadan, bulan yang diberkahi, di mana Allah mewajibkan puasa di bulan
                    itu kepada kamu. Pada bulan itu pintu-pintu langit dibuka, pintu-pintu neraka ditutup dan
                    setan-setan durhaka dibelenggu. Di bulan itu terdapat suatu malam yang lebih baik daripada seribu
                    bulan. Barangsiapa dihalangi mendapatkan kebaikannya, maka ia telah terhalangi."
                </p>
                <p>
                    2. Hadits Riwayat Bukhari Muslim

                    Dalam Hadits Riwayat Bukhari Muslim, Rasulullah SAW bersabda:

                    "Barangsiapa berpuasa Ramadan karena iman dan mengharap pahala, maka akan diampuni dosa-dosanya yang
                    telah lalu."
                </p>
                <p>
                    3. Hadits Riwayat Bukhari No. 1909

                    Diriwayatkan dari Abu Hurairah Radhiallahu ‘Anhu, bahwa Rasulullah SAW bersabda:

                    "Berpuasalah kalian karena melihatnya (hilal) dan berhari rayalah karena melihatnya, jika hilal
                    hilang dari penglihatanmu maka sempurnakan bilangan Sya’ban sampai tiga puluh hari."
                </p>


